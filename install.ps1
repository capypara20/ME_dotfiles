# ==================================================================
# Windows用 dotfiles インストーラ（骨組み）
#
# 役割: このリポジトリ内の設定ファイルを、固定パス（$PROFILE など）へ
#        シンボリックリンクで配置する。
#
# 使い方: 管理者 or 開発者モードの PowerShell で  .\install.ps1
# ==================================================================

# このスクリプトが置かれている場所（= リポジトリの場所）。
$DotDir = $PSScriptRoot

# ------------------------------------------------------------------
# 対応表:  "本体ファイル" = "配置したい固定パス"
# 増えたらここに1行足すだけ。（骨組みなので今は空）
# ------------------------------------------------------------------
$Links = @{
  # "$DotDir\windows\profile.ps1" = $PROFILE
  # "$DotDir\shared\.gitconfig"   = "$HOME\.gitconfig"
}

# ------------------------------------------------------------------
# リンク作成本体
# ------------------------------------------------------------------
foreach ($src in $Links.Keys) {
  $dst = $Links[$src]
  # ① 既存ファイルを .bak に退避
  if (Test-Path $dst) { Rename-Item $dst "$dst.bak" }
  # ② リンク作成（要管理者権限 or 開発者モード）
  New-Item -ItemType SymbolicLink -Path $dst -Target $src -Force | Out-Null
  Write-Host "リンク: $dst -> $src"
}

Write-Host "完了！（対応表が空なら何もしていません）"
